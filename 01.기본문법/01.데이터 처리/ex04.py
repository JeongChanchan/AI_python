#계산기

num1 = int(input('숫자>'))
num2 = int(input('숫자>'))

add = num1 + num2

print(f'{num1} + {num2} = {add}')
print(f'{num1} - {num2} = {num1 - num2}')
print(f'{num1} x {num2} = {num1 * num2}')
print(f'{num1} / {num2} = {num1 / num2:.3f}')
print(f'{num1} % {num2} = {num1 % num2}')
print(f'{num1} // {num2} = {num1 // num2}')

